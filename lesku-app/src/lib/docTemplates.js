export function docFooter() {
  return `<div class="docFoot"><span>Generated by LesKu · powered by Hasilin</span><span>${fmtDate(
    todayISO()
  )}</span></div>`;
}

export function levelFromScore(v) {
  v = Number(v || 0);
  if (v >= 86) return 'BSB - Berkembang Sangat Baik';
  if (v >= 70) return 'BSH - Berkembang Sesuai Harapan';
  if (v >= 55) return 'MB - Mulai Berkembang';
  if (v > 0) return 'BB - Belum Berkembang';
  return '-';
}

export function aspectComment(k, v) {
  v = Number(v || 0);
  if (!v) return 'Belum ada nilai pada periode ini.';
  if (v >= 86) return `${k} sangat baik dan perlu dipertahankan.`;
  if (v >= 70) return `${k} berkembang baik dengan latihan rutin.`;
  if (v >= 55) return `${k} mulai berkembang dan masih perlu pendampingan.`;
  return `${k} membutuhkan penguatan dasar secara bertahap.`;
}

export function makeNarrative(s, ass, lrn, att, type) {
  if (lrn.length && lrn[lrn.length - 1].parentNote)
    return lrn[lrn.length - 1].parentNote;
  const a = ass[ass.length - 1] || {};
  const score = Math.round(
    avg(
      [
        'readingScore',
        'writingScore',
        'countingScore',
        'focusScore',
        'independenceScore',
      ].map((k) => a[k])
    ) || 0
  );
  if (type === 'Rapor Akhir Periode')
    return `${
      s.name || 'Ananda'
    } menunjukkan perkembangan belajar dengan rata-rata capaian ${
      score || '-'
    }. Kekuatan utama dan area pendampingan perlu terus dipantau melalui latihan membaca, menulis, dan berhitung yang konsisten.`;
  if (score >= 80)
    return `${
      s.name || 'Ananda'
    } menunjukkan perkembangan yang baik dan semakin percaya diri mengikuti kegiatan belajar.`;
  if (score >= 65)
    return `${
      s.name || 'Ananda'
    } mulai berkembang dengan baik, namun masih membutuhkan latihan rutin agar capaian belajar lebih stabil.`;
  return `${
    s.name || 'Ananda'
  } masih memerlukan pendampingan lebih intensif, terutama pada materi dasar calistung dan konsentrasi belajar.`;
}

function scoreSummary(ass) {
  const latest = ass[ass.length - 1] || {};
  return {
    latest,
    scores: {
      Membaca: Math.round(
        avg(ass.map((a) => a.readingScore)) || latest.readingScore || 0
      ),
      Menulis: Math.round(
        avg(ass.map((a) => a.writingScore)) || latest.writingScore || 0
      ),
      Berhitung: Math.round(
        avg(ass.map((a) => a.countingScore)) || latest.countingScore || 0
      ),
      Fokus: Math.round(
        avg(ass.map((a) => a.focusScore)) || latest.focusScore || 0
      ),
      Kemandirian: Math.round(
        avg(ass.map((a) => a.independenceScore)) ||
          latest.independenceScore ||
          0
      ),
    },
  };
}

function attendanceSummaryOf(att) {
  return {
    Hadir: att.filter((a) => a.status === 'Hadir').length,
    Izin: att.filter((a) => a.status === 'Izin').length,
    Sakit: att.filter((a) => a.status === 'Sakit').length,
    Alpha: att.filter((a) => a.status === 'Alpha').length,
    Terlambat: att.filter((a) => a.status === 'Terlambat').length,
    Reschedule: att.filter((a) => a.status === 'Reschedule').length,
  };
}

