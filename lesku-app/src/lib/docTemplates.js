/** Dipakai Pusat Dokumen: lembar absensi 1 pertemuan (landscape) */
export async function documentAttendanceSheetHTML(
  classId,
  date,
  sessionNo,
  classStudents,
  attendanceRows,
  settings
) {
  const statuses = [
    'Hadir',
    'Izin',
    'Sakit',
    'Alpha',
    'Terlambat',
    'Reschedule',
  ];
  const getRecord = (studentId) =>
    attendanceRows.find(
      (a) =>
        a.date === date &&
        String(a.classId || '') === String(classId || '') &&
        a.studentId === studentId
    );
  const rows = classStudents
    .map((s, i) => {
      const rec = getRecord(s.id) || {};
      return `<tr><td>${i + 1}</td><td class="name"><b>${escapeHtml(
        s.name || '-'
      )}</b></td>${statuses
        .map((st) => `<td>${rec.status === st ? '✓' : ''}</td>`)
        .join('')}<td>${escapeHtml(rec.notes || '')}</td></tr>`;
    })
    .join('');
  return `<div class="docPaper landscape wide">${brandedDocHeader(
    settings,
    'LEMBAR ABSENSI KELAS',
    `${fmtDate(date)} · Pertemuan ${sessionNo}`
  )}<div class="docSection"><table class="docTable attendanceDoc"><thead><tr><th>No</th><th class="name">Nama Siswa</th>${statuses
    .map((s) => `<th>${s}</th>`)
    .join('')}<th>Catatan</th></tr></thead><tbody>${
    rows || '<tr><td colspan="9">Belum ada siswa.</td></tr>'
  }</tbody></table></div><div class="docSign"><div>Koordinator/Guru<br><br><br><b>(${escapeHtml(
    settings.teacherName || 'Nama Guru'
  )})</b></div><div>Mengetahui<br><br><br><b>(........................)</b></div></div>${docFooter()}</div>`;
}

/** Dipakai Pusat Dokumen: legger bulanan horizontal (landscape) */
export async function documentLedgerHTML(
  month,
  classStudents,
  attendanceRows,
  settings,
  statusCode
) {
  const [y, m] = month.split('-').map(Number);
  const days = new Date(y, m, 0).getDate();
  const head = Array.from({ length: days }, (_, i) => `<th>${i + 1}</th>`).join(
    ''
  );
  const rows = classStudents
    .map((s, i) => {
      let hadir = 0,
        non = 0,
        cells = '';
      for (let d = 1; d <= days; d++) {
        const date = `${month}-${String(d).padStart(2, '0')}`;
        const rec = attendanceRows.find(
          (a) => a.date === date && a.studentId === s.id
        );
        const code = rec ? statusCode(rec.status) : '';
        if (rec?.status === 'Hadir') hadir++;
        else if (rec?.status) non++;
        cells += `<td>${code}</td>`;
      }
      return `<tr><td>${i + 1}</td><td class="name"><b>${escapeHtml(
        s.name || '-'
      )}</b></td>${cells}<td><b>${hadir}</b></td><td><b>${non}</b></td></tr>`;
    })
    .join('');
  return `<div class="docPaper landscape wide">${brandedDocHeader(
    settings,
    'LEGGER ABSENSI BULANAN',
    month
  )}<div class="docSection"><table class="docTable attendanceDoc"><thead><tr><th>No</th><th class="name">Nama Siswa</th>${head}<th>H</th><th>Non</th></tr></thead><tbody>${
    rows || `<tr><td colspan="${days + 4}">Belum ada siswa.</td></tr>`
  }</tbody></table><p class="muted">Keterangan: H=Hadir, I=Izin, S=Sakit, A=Alpha, T=Terlambat, R=Reschedule.</p></div>${docFooter()}</div>`;
}
